	typedef struct S_Packet {
		size_t HeaderSize, OverrideSize;
		DWORD HeaderCheckSum, DataOffset;
		BYTE EncKey[0x14], IntegrityHash[0x10];
		byte DebugDataBuffer[0x27C];
		PBYTE Data;

		void __forceinline SetUint(DWORD Offset, DWORD Val) { *(PDWORD)(this->Data + Offset) = Val; }
		void __forceinline CreateIntegrityDigest(const byte *pHash) { Mem::Copy(this->IntegrityHash, pHash, Native::DecVal(0x2D8DEB42) /*0x10*/); }
		void __forceinline ComputeHeaderCheckSum() { this->HeaderCheckSum = ModRTU_CRC(this->Data, this->HeaderSize); }
		void __forceinline CreateHeader(const char* pcHeader) { this->HeaderSize = hex2bin_sec(this->Data, 0x2C, pcHeader); } //Obfuscated header
		void __forceinline SetDataOverride(void* pData, size_t Size) { Mem::Copy(this->Data + this->DataOffset, pData, Size); }
		void __forceinline SetEncryptionKey(byte *Key) { Mem::Copy(this->EncKey, Key, 0x14); }
		void __forceinline DecryptDataBlock(byte *Data, DWORD Size) { Native::RC4Custom(this->EncKey, sizeof this->EncKey, Data, Size); }
		void __forceinline XorBlock(byte *Data, size_t Length) {};
		void __forceinline ReplaceData(byte *Data) { Mem::Copy(Data, this->Data, this->OverrideSize); }

		HRESULT __forceinline VerifyHeaderCheckSum(byte *pData) { return MAKE_HRESULT_BOOL(ModRTU_CRC(pData, this->HeaderSize) == this->HeaderCheckSum); }
		HRESULT __forceinline VerifyData(byte *pData) { return MAKE_HRESULT_BOOL((Mem::Compare(pData, this->Data, this->OverrideSize) == 0)); }

		HRESULT __forceinline VerifyItegrity(PBYTE Data) {
			byte Digest[0x10];
			Native::XeCryptSha(Teapot::Session, 0x10, this->EncKey, 0x14, Data + this->DataOffset, this->OverrideSize - this->DataOffset, Digest, 0x10);
			return MAKE_HRESULT_BOOL((Mem::Compare(this->IntegrityHash, Digest, 0x10) == 0));
		}

		void __forceinline ReportViolation(int code, byte *RawPacket, bool Halt = false){
			Mem::Copy(this->DebugDataBuffer, RawPacket, this->OverrideSize);
			Mem::Copy(this->DebugDataBuffer + Native::DecVal(0xED8BEB42) /*0x150*/, this->Data, this->OverrideSize);
			Native::report(code, Halt, this->DebugDataBuffer, Native::DecVal(0xA18AEB42) /*0x27C*/);
		}

		S_Packet(const char *pcHeader, size_t iOverrideSize, DWORD DataOffset) {
			this->OverrideSize = iOverrideSize;
			this->DataOffset = DataOffset;
			this->Data = (PBYTE)Native::Malloc(iOverrideSize);
			Mem::Set(this->Data, 0, iOverrideSize);
			this->CreateHeader(pcHeader);
			this->ComputeHeaderCheckSum();
		}

		~S_Packet() {
			Mem::Set(this->EncKey, 0, 0x14);
			Mem::Set(this->Data, 0, this->OverrideSize);
			Mem::Set(this->IntegrityHash, 0, sizeof this->IntegrityHash);
			Native::Free(this->Data);
			this->Data = nullptr;
		}
	}S_Packet;

